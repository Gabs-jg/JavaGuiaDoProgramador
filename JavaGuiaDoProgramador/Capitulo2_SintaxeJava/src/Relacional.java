public class Relacional {
    public static void main(String[] args) {
        int a = 15, b = 12; //Declara e inicia as variáveis
        System.out.println("Valores: a = " + a + ", b = " + b);
        System.out.println("a == b -> " + (a == b)); //a é igual b?
        System.out.println("a != b -> " + (a != b)); //a é diferente de b?
        System.out.println("a < b -> " + (a < b)); //a é menor que b?
        System.out.println("a > b -> " + (a > b)); // a é maior que b?
        System.out.println("a <= b -> " + (a <= b)); //a é menor ou igual a b?
        System.out.println("a >= b -> " + (a >= b)); //a é maior ou igual a b?
    }
}
